    document.getElementById('social-category-sc').textContent = options.socialCategorySC;
    document.getElementById('social-category-st').textContent = options.socialCategoryST;
    document.getElementById('social-category-obc').textContent = options.socialCategoryOBC;
    document.getElementById('social-category-ews').textContent = options.socialCategoryEWS;

