    document.getElementById('employment-status-daily-wage-worker').textContent = options.employmentStatusDailyWageWorker;
    document.getElementById('employment-status-farmer').textContent = options.employmentStatusFarmer;
    document.getElementById('employment-status-other').textContent = options.employmentStatusOther;

